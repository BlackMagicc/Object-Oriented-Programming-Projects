# Math expressions

x = 250
ml = 0.202884
tsp = ml * x
tbsp = tsp / 3
cup = tbsp / 16
pint = cup / 2
quart = pint / 2
gallon = quart / 4
fl_oz = x / 29.5735

# I do all of my conversions here, I made x 250 because that's our constant

print("mL: " + str(x))
print("tsp: " + str(tsp))
print("tbsp: " + str(tbsp))
print("cup(s): " + str(cup))
print("pint(s): " + str(pint))
print("quart(s): " + str(quart))
print("gallon(s): " + str(gallon))
print("fl oz: " + str(fl_oz))

